package Irbdd;

public class Irbdd {

    public static void main(String[] args) {

        for (int i = 1; i < 10; i++) {
            if (i == 5) break;
            System.out.println(i);
        }

        int a = 10;
        while (a > 0) {
            if(a == 5)break;
            System.out.println(a);
            a--;
        }
        
    }

}
