package day26;
public class Day26 {

    public static void main(String[] args) {
       
        int a = 10; 
        int b = 7;
        
        System.out.println(a == b); // false
        System.out.println(a != b); // true
        System.out.println(a < b); // false
        System.out.println(a > b); // true
        System.out.println(a >= b); // true
        System.out.println(a <= b); // false    
    }
    
}
