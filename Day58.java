package Irbdd;

public class Irbdd {

    public static void main(String[] args) {

        int n = 5;

        for (int i = 1; i <= n; i++) {
            System.out.print(i + " ");
        }
        System.out.println(" ");
        for (int i = n; i > 0; i--) {
            System.out.print(i + " ");
        }
        System.out.println(" ");
    }

}
